package BE;

public class Title {
    int Id;
    String title;

    public Title(int id, String title) {
        Id = id;
        this.title = title;
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
